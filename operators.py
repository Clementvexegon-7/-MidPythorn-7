#Arithmetic Operators - Simple calculations

x = 34
y = 20

print(x + y )
print(x - y )
print(x * y )
print(x / y )
print(x % y )#Modulas - Returns the remainder after division

#Comparison operators - Compare values
print(x < y )
print(x > y )
print(x <= y )
print(x >= y )
print(x == y )#Equal to
print(x != y )#Not equal to

#Assignment Operator
number = 10
print(number)

#Operator Precedence
number += 2
print(number)

#Logic Operators - and, or, not
num1 = 60
num2 = 12

print( num1 == num2 and  num1 > num2)
print( num1 == num2 or  num1 > num2)
print(not(num1 == num2 or  num1 > num2))

